CREATE TABLE evaluation_dataset (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description VARCHAR(500),
    version INTEGER NOT NULL DEFAULT 1,
    enabled BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_evaluation_dataset_name UNIQUE (name),
    CONSTRAINT ck_evaluation_dataset_version
        CHECK (version >= 1)
);

CREATE INDEX idx_evaluation_dataset_enabled_created
    ON evaluation_dataset (enabled, created_at DESC, id DESC);

CREATE TABLE evaluation_case (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dataset_id BIGINT NOT NULL,
    case_code VARCHAR(120) NOT NULL,
    case_type VARCHAR(48) NOT NULL,
    target_payload JSONB NOT NULL DEFAULT '{}'::JSONB,
    expected_payload JSONB NOT NULL DEFAULT '{}'::JSONB,
    notes VARCHAR(500),
    enabled BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_evaluation_case_dataset
        FOREIGN KEY (dataset_id) REFERENCES evaluation_dataset (id) ON DELETE CASCADE,
    CONSTRAINT uq_evaluation_case_dataset_code UNIQUE (dataset_id, case_code),
    CONSTRAINT ck_evaluation_case_target_payload_object
        CHECK (jsonb_typeof(target_payload) = 'object'),
    CONSTRAINT ck_evaluation_case_expected_payload_object
        CHECK (jsonb_typeof(expected_payload) = 'object'),
    CONSTRAINT ck_evaluation_case_type
        CHECK (case_type IN (
            'CRAWL_ITEM_PRESENT',
            'CLUSTER_MEMBERSHIP',
            'SCORE_THRESHOLD',
            'ANALYSIS_REQUIRED_FIELDS',
            'ALERT_EXPECTED_RECORD'
        ))
);

CREATE INDEX idx_evaluation_case_dataset_enabled
    ON evaluation_case (dataset_id, enabled, id DESC);

CREATE TABLE evaluation_run (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dataset_id BIGINT NOT NULL,
    status VARCHAR(24) NOT NULL,
    total_cases INTEGER NOT NULL DEFAULT 0,
    passed_cases INTEGER NOT NULL DEFAULT 0,
    failed_cases INTEGER NOT NULL DEFAULT 0,
    error_cases INTEGER NOT NULL DEFAULT 0,
    metrics_payload JSONB NOT NULL DEFAULT '{}'::JSONB,
    error_analysis_payload JSONB NOT NULL DEFAULT '{}'::JSONB,
    started_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    finished_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_evaluation_run_dataset
        FOREIGN KEY (dataset_id) REFERENCES evaluation_dataset (id) ON DELETE CASCADE,
    CONSTRAINT ck_evaluation_run_status
        CHECK (status IN ('RUNNING', 'COMPLETED', 'FAILED')),
    CONSTRAINT ck_evaluation_run_counts
        CHECK (total_cases >= 0
            AND passed_cases >= 0
            AND failed_cases >= 0
            AND error_cases >= 0),
    CONSTRAINT ck_evaluation_run_metrics_object
        CHECK (jsonb_typeof(metrics_payload) = 'object'),
    CONSTRAINT ck_evaluation_run_error_analysis_object
        CHECK (jsonb_typeof(error_analysis_payload) = 'object')
);

CREATE INDEX idx_evaluation_run_dataset_created
    ON evaluation_run (dataset_id, created_at DESC, id DESC);

CREATE TABLE evaluation_case_result (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id BIGINT NOT NULL,
    case_id BIGINT NOT NULL,
    case_code VARCHAR(120) NOT NULL,
    case_type VARCHAR(48) NOT NULL,
    status VARCHAR(24) NOT NULL,
    actual_payload JSONB NOT NULL DEFAULT '{}'::JSONB,
    failure_reason VARCHAR(500),
    evaluated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_evaluation_case_result_run
        FOREIGN KEY (run_id) REFERENCES evaluation_run (id) ON DELETE CASCADE,
    CONSTRAINT ck_evaluation_case_result_status
        CHECK (status IN ('PASSED', 'FAILED', 'ERROR')),
    CONSTRAINT ck_evaluation_case_result_actual_object
        CHECK (jsonb_typeof(actual_payload) = 'object')
);

CREATE INDEX idx_evaluation_case_result_run
    ON evaluation_case_result (run_id, id DESC);
